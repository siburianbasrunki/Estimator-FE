interface PekerjaanItem {
  kode: string;
  deskripsi: string;
  satuan: string;
  harga: number;
}

interface PekerjaanData {
  [key: string]: PekerjaanItem[];
}

export const flattenToDropdown = (data: PekerjaanData) => {
  return Object.values(data).flatMap(category => 
    category.map(item => ({
      kode: item.kode,
      label: `${item.deskripsi} - Rp${item.harga.toLocaleString('id-ID')}/${item.satuan}`,
      value: item.kode,
      detail: item
    }))
  );
};


